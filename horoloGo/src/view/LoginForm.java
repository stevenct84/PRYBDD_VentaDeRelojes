  /**
   * Class panelBackground.
   * Inner class that creates a background image to the window.
   */
  
  class PanelBackground extends JPanel {
    private Image backgroundImage;
    
    @Override
    public void paint(Graphics grph) {
      backgroundImage = new ImageIcon(getClass().getResource("/images/loginForm2.jpg")).getImage();
      grph.drawImage(backgroundImage, 0, 0, getWidth(), getHeight(), this);
      setOpaque(false);
      
      super.paint(grph);
    }
  }
